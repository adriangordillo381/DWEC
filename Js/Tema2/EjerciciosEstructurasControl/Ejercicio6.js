/*
Necesitamos un programa que muestre los números del 10 al 20, ambos inclusive. Y a
continuación, muestre los números del 20 al 10, es decir, de mayor a menor.
*/

for (let i = 10; i <= 20; i++) {
  document.write(i);
}
document.write("<br>");
for (let j = 20; j >= 10; j--) {
  document.write(j);
}
